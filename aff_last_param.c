#include <unistd.h>

int	main(int argc, char **argv)
{
	int i;
	int n;

	i = 0;
	if (argc < 2)
	{
		write(1, "\n", 1);
	}
	else
	{
		n = argc - 1;
		while (argv[n][i] != '\0')
		{
			write(1, &argv[n][i], 1);
			i++;
		}
	}
	return (0);
}
